#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn counters_increment_and_add() {
        let m = Metrics::new();
        m.inc_request_ok();
        m.inc_request_ok();
        m.inc_request_error();
        m.add_prompt_tokens(100);
        m.add_prompt_tokens(50);
        m.add_gen_tokens(25);

        assert_eq!(m.requests_ok.load(Ordering::Relaxed), 2);
        assert_eq!(m.requests_error.load(Ordering::Relaxed), 1);
        assert_eq!(m.prompt_tokens_total.load(Ordering::Relaxed), 150);
        assert_eq!(m.gen_tokens_total.load(Ordering::Relaxed), 25);
    }

    #[test]
    fn ttft_ring_wraps_at_capacity() {
        let mut ring = TtftRing::new(4);
        for i in 0..6u64 {
            ring.push(i * 10); // 0, 10, 20, 30, 40, 50
        }
        // After 6 pushes on a cap-4 ring: buf = [40, 50, 20, 30], filled=true.
        let snap = ring.snapshot();
        assert_eq!(snap.len(), 4);
        let mut sorted = snap.clone();
        sorted.sort_unstable();
        assert_eq!(sorted, vec![20, 30, 40, 50]);
    }

    #[test]
    fn ttft_snapshot_before_filled_returns_prefix_only() {
        let mut ring = TtftRing::new(10);
        ring.push(5);
        ring.push(7);
        ring.push(9);
        let snap = ring.snapshot();
        assert_eq!(snap, vec![5, 7, 9]);
    }

    #[test]
    fn quantiles_empty_are_all_zero() {
        let qs = compute_quantiles(&[], &[0.5, 0.95]);
        assert_eq!(qs, vec![0, 0]);
    }

    #[test]
    fn quantiles_sorted_input_p50_p95() {
        // 100 samples, uniform 1..=100. p50 = 50 or 51 depending on
        // interpolation; R-7 on positions 0..99 gives pos = 0.5*99 = 49.5,
        // so (samples[49]+samples[50])/2 = (50+51)/2 = 50.5 → 51 after round.
        let v: Vec<u32> = (1..=100u32).collect();
        let qs = compute_quantiles(&v, &[0.5, 0.95]);
        assert_eq!(qs[0], 51); // p50 rounded
        // p95 at pos = 0.95*99 = 94.05 → samples[94] + 0.05*(samples[95]-samples[94])
        //                          = 95 + 0.05*1 = 95.05 → 95.
        assert_eq!(qs[1], 95);
    }

    #[test]
    fn render_prometheus_has_expected_families() {
        let m = Metrics::new();
        m.inc_request_ok();
        m.add_prompt_tokens(42);
        m.add_gen_tokens(13);
        m.observe_ttft_ms(120);
        m.observe_ttft_ms(250);

        let body = m.render_prometheus(1, 4, 0);
        assert!(body.contains("chimere_requests_total{status=\"ok\"} 1"));
        assert!(body.contains("chimere_requests_total{status=\"error\"} 0"));
        assert!(body.contains("chimere_prompt_tokens_total 42"));
        assert!(body.contains("chimere_gen_tokens_total 13"));
        assert!(body.contains("chimere_slot_occupancy 1"));
        assert!(body.contains("chimere_slot_pool_size 4"));
        assert!(body.contains("chimere_admission_queue_depth 0"));
        assert!(body.contains("chimere_ttft_seconds{quantile=\"0.50\"}"));
        assert!(body.contains("chimere_ttft_seconds_count 2"));
    }

    #[test]
    fn render_prometheus_omits_ttft_when_ring_empty() {
        let m = Metrics::new();
        let body = m.render_prometheus(0, 1, 0);
        assert!(!body.contains("chimere_ttft_seconds"));
    }

    #[test]
    fn snapshot_json_shape_is_stable() {
        let m = Metrics::new();
        m.inc_request_ok();
        m.observe_ttft_ms(100);
        let v = m.snapshot_json(2, 4, 3);
        assert_eq!(v["requests_ok"], 1);
        assert_eq!(v["slot_occupancy"], 2);
        assert_eq!(v["slot_pool_size"], 4);
        assert_eq!(v["admission_queue_depth"], 3);
        assert_eq!(v["ttft"]["count"], 1);
    }

    #[test]
    fn snapshot_json_ttft_null_when_empty() {
        let m = Metrics::new();
        let v = m.snapshot_json(0, 1, 0);
        assert!(v["ttft"].is_null());
    }
}
